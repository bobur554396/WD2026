# Iterable objects/Collections
a = [3, 3.8, 'hi', True] # list
b = (3, 3.8, 'hi', True) # tuple - immutable list
c = {3, 3.8, 'hi', True} # set - unique list of values
d = {"name": "KBTU", "address": "Tole bi", "type": "University"} # dict/dictionary/object/map

print(type(a))
print(type(b))
print(type(c))
print(type(d))

